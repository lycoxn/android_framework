    private static void install(Context context, File savePath) {
        Uri uri;
        Intent intent = new Intent();
        intent.setAction("android.intent.action.VIEW");
        intent.addCategory("android.intent.category.DEFAULT");
        intent.setType("application/vnd.android.package-archive");
        if (Build.VERSION.SDK_INT < Build.VERSION_CODES.N) { // 6.0以下
            uri = Uri.fromFile(savePath);
            intent.setFlags(Intent.FLAG_ACTIVITY_NEW_TASK);
        } else { // Android 7.0 以上
            intent.setFlags(Intent.FLAG_GRANT_READ_URI_PERMISSION | Intent.FLAG_GRANT_WRITE_URI_PERMISSION | Intent.FLAG_ACTIVITY_NEW_TASK);
            uri = FileProvider.getUriForFile(context, "com.chanjet.good.collecting.fuwushang.fileProvider", savePath);
        }
        intent.setDataAndType(uri, "application/vnd.android.package-archive");
        context.startActivity(intent);
    }

