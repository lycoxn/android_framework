    /*
     *计算time2减去time1的差值 差值只设置 几天 几个小时 或 几分钟
     * 根据差值返回多长之间前或多长时间后
     * 决定是否重新获取私钥
     * */
    public static boolean getDistance(long time1, long time2) {
        long hour = 0;
        long diff;
        if (time1 < time2) {
            diff = time2 - time1;
        } else {
            diff = time1 - time2;
        }
        hour = (diff / (60 * 60 * 1000));
        if (hour != 0) {
            if (hour > 8) {
                return true;
            }
        }
        return false;
    }

